describe("EMBRYO UTILS: $.extend", function() {
    it("$.extend({'k1':'v1'}, {'k2':'v2'}) should return {'k1':'v1', 'k2':'v2'}", function() {
        expect( $.extend({'k1':'v1'}, {'k2':'v2'}) ).toEqual( {'k1':'v1', 'k2':'v2'} );
    });
});
